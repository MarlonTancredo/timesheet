import mongoose, { Model, Schema } from "mongoose";

const UserSchema: Schema = new mongoose.Schema({
  name: {
    type: String,
    require: true,
  },
  surname: {
    type: String,
    require: true,
  },
  email: {
    type: String,
    require: true,
  },
  confirmEmail: {
    type: String,
    require: true,
  },
  password: {
    type: String,
    require: true,
  },
  confirmPassword: {
    type: String,
    require: true,
  },
});

const UserModel = mongoose.model("users", UserSchema);

export default UserModel;
